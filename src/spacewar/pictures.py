"""pictures.py — sprite and animation data.

Sprites are rendered at 1:1 screen pixels (no Y-scale) so they appear correct on our 640×480 screen.

Sprite formats
--------------
Ships (Enterprise / Klingon)
    16 rotation frames × 16 rows × 16-bit row.  Bit 15 = leftmost pixel.
    Frame 0 = pointing right (angle 0), frames advance by 22.5° (=16/256 circle).

Torpedoes (ETOR / KTOR)
    8 rotation frames × 8 rows × 8-bit row.  Bit 7 = leftmost pixel.
    Frame 0 = right, frames advance by 45°.

Explosions (EXPD)
    8 animation frames × 8 rows × 8-bit row.

Planet (PLANET_PICTURES)
    16 animation frames × 32 rows × 32-bit row.  Bit 31 = leftmost pixel.

Public API
----------
get_enterprise_sprite(angle) -> list[int]   16 rows, 16-bit
get_klingon_sprite(angle)    -> list[int]   16 rows, 16-bit
get_enterprise_torp_sprite(angle) -> list[int]  8 rows, 8-bit
get_klingon_torp_sprite(angle)    -> list[int]  8 rows, 8-bit
get_explosion_frame(n)       -> list[int]   8 rows, 8-bit
get_planet_frame(n)          -> list[int]   32 rows, 32-bit
"""

